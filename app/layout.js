import localFont from "next/font/local";
import "./globals.css";
import { cookies } from "next/headers";
import LogoutBtn from "./../components/LogoutBtn";

export const metadata = {
  title: "Create Next App",
  description: "Generated by create next app",
};

export default function RootLayout({ children }) {
  const logged_user = cookies().get("sid")?.value;

  function parseUser() {
    if (!logged_user) return undefined;
    return JSON.parse(logged_user);
  }

  const user = parseUser();

  return (
    <html lang="en">
      <body>
        <nav className="p-4 shadow bg-white text-black mb-1 flex justify-between items-center sticky top-0">
          <span>
            {user ? user.firstName + " " + user.lastName : "Please sign up and chat with "}
          </span>
          <span className="text-blue-500 font-extrabold font-serif">
            JULA-BOT
          </span>
          {user && (
            <>
              <LogoutBtn />
            </>
          )}
        </nav>
        <main>{children}</main>
      </body>
    </html>
  );
}
